
def add (x, y):
    """Add Function"""
    return x + y


def subtract (x, y):
    """Subtract Function"""
    return x - y


def multiply (x, y):
    """Multiply Function"""
    return x * y


def divide (x, y):
    """Divide Function"""
    if y == 0:
        return "Real numbers are not divisible by zero"
    else:
        return x / y


def floor (x, y):
    """Floor Fuction"""
    return x // y


def power (x, y):
    """Divide Function"""
    return x ** y
